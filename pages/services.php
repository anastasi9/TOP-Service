<!-- Скрипт для поиска с подсветкой -->
<script>
function filterServices() {
    const input = document.getElementById('serviceSearch');
    const filter = input.value.toLowerCase();

    document.querySelectorAll('.service-category').forEach(category => {
        const items = category.querySelectorAll('.service-item');
        let visibleCount = 0;

        items.forEach(item => {
            const text = item.textContent;
            if (filter === '') {
                item.innerHTML = text;
                item.style.display = '';
                visibleCount++;
            } else if (text.toLowerCase().indexOf(filter) > -1) {
                // Выделяем совпадение
                const regex = new RegExp(`(${filter})`, 'gi');
                item.innerHTML = text.replace(regex, '<mark>$1</mark>');
                item.style.display = '';
                visibleCount++;
            } else {
                item.style.display = 'none';
            }
        });

        // Скрываем заголовок категории, если все услуги скрыты
        if (visibleCount === 0) {
            category.style.display = 'none';
        } else {
            category.style.display = '';
        }
    });
}
</script>
